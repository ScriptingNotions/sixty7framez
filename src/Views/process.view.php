<section class="process-section-1">
    <h1>01</h1>
    <div>
        <h2>Booking the Photo Booth</h2>
        <p><span>Inquiry:</span>Clients start by inquiring about our photo booth service via our website, phone, or email.</p>
        <p><span>Availability Check:</span>Check availability for the event date using our booking tool.</p>
        <p><span>Package Selection:</span>Clients choose a package (e.g., number of hours, types of backdrops, and photo print options).</p>
        <p><span>Customization:</span>Additional features like custom photo frames, digital props, or themed backdrops can be selected.</p>
        <p><span>Contract and Payment:</span>A contract is signed, and a deposit or full payment is made to confirm the booking.</p>
    </div>
</section>

